#Os operadores de associação são utilizados para verificar se uma sequência contém um objeto.
frutas = ["banana","laranja","uva","ameixa"]

fruta_1 = "ameixa"
fruta_2 = "melancia"

print(fruta_1 in frutas) # True
print(fruta_2 in frutas) # False

#O exemplo acima verifica a existência de variáveis dentro de uma lista.
#  Na linha 7 estamos checando se fruta_1 está contido em frutas.
#  Da mesma forma, fazemos isso na linha 8, só que dessa vez com fruta_2.