# O operador de atribução é utilizado quando queremos alterar o valor da variável
numero = 5

numero -=1 # 4
print(numero)

#Operador	Exemplo	Equivalente a
# =	         x = 1	x = 1
# +=         x += 1	x = x + 1
# -=         x -= 1	x = x - 1
# *=	     x *= 1	x = x * 1
# /=	     x /= 1	x = x / 1
# %=	     x %= 1	x = x % 1