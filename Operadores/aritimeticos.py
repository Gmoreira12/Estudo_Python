
numero_1 = 6
numero_2 = 4

soma = numero_1 + numero_2  # 10
subtracao = numero_1 - numero_2  # 2
multiplicacao = numero_1 * numero_2  # 24
divisao = numero_1 / numero_2  # 1.5
# 1 | Realiza a divisão entre operandos e a parte decimal do resultado
divisao_inteira = numero_1 // numero_2
modulo = numero_1 % numero_2  # 2 | Retorna o resto da divisão entre operandos
# 1296 | Retorna um número elevado a potência de outro
exponenciacao = numero_1 ** numero_2

print(soma)
print(subtracao)
print(multiplicacao)
print(divisao)
print(divisao_inteira)
print(modulo)
print(exponenciacao)
