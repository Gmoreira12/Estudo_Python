# O laço for nos permite percorrer os itens de uma coleção e, para cada um deles,
# executar o bloco de código declarado no loop.

linguagens = ['python','C#','JAVA','Javascript']
for i in linguagens:
    print(i)
else:
    print('Lista completa de linguagens')
print('\n')

for i in range(0,7): # O método "range" cria um intervalo de execução.
    print(i)
else:
    print('Os números de 0 a 6 foram listados')
