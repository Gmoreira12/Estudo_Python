# Com a função type() podemos obter o tipo do objeto passado no parâmetro.

dias = ('Segunda', 'Terça', 'Quanta', 'Quinta', 'Sexta', 'Sabado')
materias =['Python', 'Lógica', 'SQL', 'CSS', 'HTML', 'GIT']
assinatura = {
    'Devmedia': 'Gmoraira1',
    'E-mail': 'goncalves@gmail.com'
}

print(type(dias)) # Tupla
print(type(materias)) # Lista
print(type(assinatura)) # Dicionário