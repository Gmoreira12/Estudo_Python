# Outra característica das listas no Python é que elas são mutáveis,
# podendo ser alteradas depois de terem sido criadas.
# Em outras palavras, podemos adicionar, remover e até mesmo alterar os itens de uma lista.
programadores = ['Gabriel', 'Paulo', 'Sandro']
print(programadores)

programadores[1] = 'Julia'
print(programadores) # Nessa segunda parte o nome "Julia" entra no lugar do nome "Paulo" que está no índice "1".

